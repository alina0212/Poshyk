"""
Конфігурація SemanticUA — пошук по документу (Bi-Encoder + FAISS + Cross-Encoder).

Кожен параметр у блоці "Пошук" має коментар:
  • Що робить           — за що відповідає
  • Чому це значення    — на чому базується
  • Якщо збільшити/зменшити — побічні ефекти
"""

import os


# ════════════════════════════════════════════════════════
# ФАЙЛОВА СИСТЕМА
# ════════════════════════════════════════════════════════

BASE_DIR      = os.path.dirname(__file__)
UPLOAD_FOLDER = os.path.join(BASE_DIR, "uploads")   # тимчасові файли при upload
CACHE_DIR     = os.path.join(BASE_DIR, "cache")     # FAISS-індекс + чанки на диск
os.makedirs(UPLOAD_FOLDER, exist_ok=True)
os.makedirs(CACHE_DIR,     exist_ok=True)

FAISS_CACHE_PATH   = os.path.join(CACHE_DIR, "faiss.index")
SESSION_CACHE_PATH = os.path.join(CACHE_DIR, "session.json")


# ════════════════════════════════════════════════════════
# ЛІМІТИ ВВОДУ
# ════════════════════════════════════════════════════════

MAX_CONTENT_LENGTH = 32 * 1024 * 1024   # 32 MB — максимум на upload
ALLOWED_EXTENSIONS = {".txt", ".pdf", ".docx", ".doc", ".md"}


# ════════════════════════════════════════════════════════
# ЧАНКУВАННЯ ДОКУМЕНТА
# ════════════════════════════════════════════════════════

# CHUNK_MIN_LEN — мінімальна довжина фрагмента (символів).
#   Що робить:  Короткі абзаци склеюються поки не досягнуть цього розміру.
#   Чому 60:    Менші фрагменти не несуть контексту (заголовки, посилання).
#   Збільшити → менше чанків, гірша точність для коротких ідей.
CHUNK_MIN_LEN = 60

# CHUNK_MAX_LEN — максимальна довжина фрагмента.
#   Що робить:  Якщо абзац довший, ріжемо по реченнях, потім sliding window.
#   Чому 600:   E5/BGE мають max_length=512 токенів ≈ 600-800 символів.
#   Збільшити → токени обрізаються моделлю, втрата інформації в кінці.
CHUNK_MAX_LEN = 600


# ════════════════════════════════════════════════════════
# ПАРАМЕТРИ ПОШУКУ
# ════════════════════════════════════════════════════════

# SEARCH_TOP_K_BI — скільки кандидатів FAISS повертає для CE reranking.
#   Що робить:  Bi-Encoder сортує всі чанки, top-K йдуть у Cross-Encoder.
#   Чому 60:    Збалансовано: CE на CPU = ~50мс/пара → 60 × 50мс = ~3 сек.
#   Збільшити (100+) → ловимо більше слабких кандидатів, але +5-7 сек на запит.
#   Зменшити  (30)   → швидше, але можемо втратити релевантні в хвості.
SEARCH_TOP_K_BI = 60

# SEARCH_PER_PAGE — кількість результатів на сторінку UI.
SEARCH_PER_PAGE = 5


# ── Пороги Cross-Encoder (BGE-reranker-v2-m3) ───────────
# BGE на українській видає скори у вузькому діапазоні sigmoid(50.0-57.0):
#   50.0         = logit ≈ 0 → "невпевнений / нерелевантно"
#   50.5-52.0    = слабка релевантність (синоніми, контекстні зв'язки)
#   53.0+        = сильна релевантність (прямі збіги змісту)

# SEMANTIC_THRESHOLD — нижня абсолютна межа CE-скору.
#   Що робить:  Жорсткий floor; навіть якщо адаптивне вікно опускається нижче.
#   Чому 0.503: Дрібний поріг над шумом (50.0). Все нижче — артефакти.
#   Збільшити (0.55) → менше результатів, але точніші.
#   Зменшити  (0.50) → ловимо шум типу "двері" в документі без дверей.
SEMANTIC_THRESHOLD = 0.503

# QUERY_RELEVANCE_GATE — якщо топ-CE нижче цього, повертаємо 0 результатів.
#   Що робить:  Захист від нерелевантних запитів. "Двері" → топ-CE=50.0 → []
#   Чому 50.4:  BGE на irrelevant парах видає рівно 50.0. Релевантні від 50.6.
#   Збільшити (51.0) → "фото"=50.6 теж відсіється (false negative).
#   Зменшити  (50.2) → пропустить запити, які насправді нерелевантні.
QUERY_RELEVANCE_GATE = 50.4

# ADAPTIVE_CE_WINDOW — наскільки нижче топ-CE можна брати результати.
#   Що робить:  Якщо top_ce=57.1, беремо все що >= 57.1 - 7.0 = 50.1.
#   Чому 7.0:   BGE робить різкий розрив: топ-1 ~57, інші ~51-52.
#                Вузьке вікно (3-5) залишило б тільки топ-1.
#   Збільшити (10) → більше результатів, але слабких.
#   Зменшити  (3)  → тільки найкращі, втрата recall.
ADAPTIVE_CE_WINDOW = 7.0

# BI_MIN_PCT — мінімальний bi_pct для проходження в CE стадію.
#   Що робить:  Pre-filter перед CE щоб скоротити роботу.
#   Чому 0:     E5 на українській має baseline cosine ~0.80 для будь-якої пари
#                (anisotropic embeddings). Фільтр практично нічого не відсіює.
#                Лишено в коді для майбутнього (інша Bi-модель може мати інший baseline).
#   Поставити 70+ → суттєвий вплив тільки якщо переключити Bi-модель на менш anisotropic.
BI_MIN_PCT = 0


# ════════════════════════════════════════════════════════
# ML-МОДЕЛІ
# ════════════════════════════════════════════════════════

# Bi-Encoder: E5-base
#   100+ мов, SOTA-якість серед моделей <300MB.
#   Особливість: вимагає префіксів "query: " та "passage: " (інакше -10-20% якості).
BI_ENCODER_MODEL = "intfloat/multilingual-e5-base"
E5_QUERY_PREFIX   = "query: "
E5_PASSAGE_PREFIX = "passage: "

# Cross-Encoder: BGE-reranker-v2-m3
#   100+ мов, в т.ч. українська. ~568MB.
#   На українській видає плоскі скори, тому використовується як gate, не ranker.
CE_MODEL      = "BAAI/bge-reranker-v2-m3"
CE_MAX_LENGTH = 512   # обмеження вхідних токенів (більше — повільніше + truncate)


# ════════════════════════════════════════════════════════
# ВЕРСІОНУВАННЯ КЕШУ
# ════════════════════════════════════════════════════════

# При зміні bi-моделі ембединги несумісні зі старим FAISS-індексом.
# Версія включає назву моделі — при заміні BI_ENCODER_MODEL кеш авто-видаляється.
MODEL_VERSION = f"{BI_ENCODER_MODEL}::v1"
